#include <stdlib.h>

char	*ft_strchr(const char *s, int c)
{
	int		idx;

	idx = 0;
	while (s[idx] && s[idx] != c)
		idx++;
	return ((s[idx] == c) ? (char*)&(s[idx]) : NULL);
}
