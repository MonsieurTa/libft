#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	ref;

	ref = c;
	while (*s)
		if (ref == *s++)
			return ((char*)--s);
	return ((*s == 0 && ref == 0) ? (char*)s : NULL);
}
